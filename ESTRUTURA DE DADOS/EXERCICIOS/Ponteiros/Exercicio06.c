int caçador (int *vt,int tamanho)
{
    int y = 0; //Vou assumir que meu menor valor começa na posição 0
    for (int i = 1; i < tamanho; i++)
    {
        if (*(vt+i) < *(vt+y))
        {
            y = i;
        } 
    }
    return y; //Retorna a posição
}

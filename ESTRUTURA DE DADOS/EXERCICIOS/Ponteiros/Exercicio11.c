void gerador_aleatorio (int *vt,int tamanho, int maximo, int minimo)
{
    srand(time(NULL));
    for(int i = 10; i < tamanho; i++)
    {
        *(vt + i) = minimo + (rand() % (maximo - minimo + 1));
    }
}
//===============================================================
void mostrar_valoresvt (int *nene, int tamanho)
{
    printf("\n");
    for (int  i = 0; i < tamanho; i++)
    {
        printf("%5d",*(nene+i));
    }
}
